def _split_command(text: str):
    parts = (text or "").strip().split()
    if not parts:
        return []
    parts[0] = parts[0].split("@")[0]
    return parts

def _fmt_price(v: float) -> str:
    try:
        if v < 0.01: return f"${v:.8f}"
        if v < 1:    return f"${v:.6f}"
        return f"${v:,.2f}"
    except Exception:
        return str(v)

def calculate_analysis_score(rsi: float, macd_data, bb_data, volume_data, current_price: float, sma_20: float) -> tuple:
    """Geli≈ümi≈ü skor hesaplama"""
    score = 5.0
    signals = []
    
    # RSI Analizi
    if rsi < 30:
        score += 2.5
        signals.append("RSI a≈üƒ±rƒ± satƒ±m ‚úÖ")
    elif rsi < 40:
        score += 1.5
        signals.append("RSI d√º≈ü√ºk")
    elif rsi > 70:
        score -= 2.5
        signals.append("RSI a≈üƒ±rƒ± alƒ±m ‚ö†Ô∏è")
    elif rsi > 60:
        score -= 1.5
        signals.append("RSI y√ºksek")
    
    # MACD Analizi
    try:
        macd_current = macd_data['macd'].iloc[-1]
        macd_signal = macd_data['signal'].iloc[-1]
        macd_prev = macd_data['macd'].iloc[-2]
        signal_prev = macd_data['signal'].iloc[-2]
        
        if macd_current > macd_signal:
            score += 1.5
            if macd_prev <= signal_prev:  # Crossover
                score += 1
                signals.append("MACD pozitif kesi≈üim üéØ")
            else:
                signals.append("MACD pozitif")
        else:
            score -= 1.5
            if macd_prev >= signal_prev:  # Crossunder
                score -= 1
                signals.append("MACD negatif kesi≈üim ‚ö†Ô∏è")
            else:
                signals.append("MACD negatif")
    except:
        pass
    
    # Bollinger Bands
    try:
        upper = bb_data['upper'].iloc[-1]
        lower = bb_data['lower'].iloc[-1]
        
        if current_price <= lower:
            score += 2
            signals.append("Bollinger alt bandƒ±nda üéØ")
        elif current_price >= upper:
            score -= 2
            signals.append("Bollinger √ºst bandƒ±nda ‚ö†Ô∏è")
    except:
        pass
    
    # SMA Trend
    if current_price > sma_20:
        score += 1
        signals.append("SMA20 √ºzerinde")
    else:
        score -= 1
        signals.append("SMA20 altƒ±nda")
    
    # Volume Analizi
    if volume_data.get('volume_ratio', 1) > 1.5:
        signals.append("Y√ºksek hacim üìä")
        score += 0.5
    elif volume_data.get('volume_ratio', 1) < 0.5:
        signals.append("D√º≈ü√ºk hacim ‚ö†Ô∏è")
        score -= 0.5
    
    # Skoru 0-10 aralƒ±ƒüƒ±na sƒ±nƒ±rla
    score = max(0, min(10, score))
    
    return score, signals

def get_multi_timeframe_analysis(symbol: str) -> dict:
    """√áoklu timeframe analizi"""
    timeframes = {
        '1h': {'limit': 100, 'weight': 0.3},
        '4h': {'limit': 100, 'weight': 0.3},
        '1d': {'limit': 100, 'weight': 0.25},
        '1w': {'limit': 52, 'weight': 0.15}
    }
    
    results = {}
    
    for tf, config in timeframes.items():
        try:
            df = get_binance_ohlc(symbol, interval=tf, limit=config['limit'])
            if df is None or df.empty:
                continue
            
            # Temel hesaplamalar
            current_price = float(df['close'].iloc[-1])
            rsi = float(calculate_rsi(df['close']).iloc[-1])
            macd = calculate_macd(df['close'])
            bb = calculate_bollinger_bands(df['close'])
            sma20 = float(calculate_sma(df['close'], 20).iloc[-1])
            volume_data = calculate_volume_analysis(df)
            
            # Skor hesapla
            score, signals = calculate_analysis_score(rsi, macd, bb, volume_data, current_price, sma20)
            
            # MACD durumu
            macd_status = "‚Üë" if macd['macd'].iloc[-1] > macd['signal'].iloc[-1] else "‚Üì"
            
            results[tf] = {
                'score': score,
                'rsi': rsi,
                'macd_status': macd_status,
                'signals': signals[:2],  # ƒ∞lk 2 sinyal
                'price': current_price,
                'volume_ratio': volume_data.get('volume_ratio', 1)
            }
        except Exception as e:
            print(f"Timeframe {tf} analiz hatasƒ±: {e}")
            continue
    
    return results

def calculate_risk_metrics(df: pd.DataFrame, current_price: float) -> dict:
    """Risk metriklerini hesapla"""
    try:
        # Volatilite hesapla (ATR benzeri)
        high_low = df['high'] - df['low']
        high_close = abs(df['high'] - df['close'].shift())
        low_close = abs(df['low'] - df['close'].shift())
        ranges = pd.concat([high_low, high_close, low_close], axis=1)
        true_range = ranges.max(axis=1)
        atr = true_range.rolling(14).mean().iloc[-1]
        
        # Volatilite y√ºzdesi
        volatility_pct = (atr / current_price) * 100
        
        # Risk skoru (0-10)
        if volatility_pct < 2:
            risk_score = 3
            risk_level = "D√º≈ü√ºk"
        elif volatility_pct < 3:
            risk_score = 5
            risk_level = "Orta"
        elif volatility_pct < 5:
            risk_score = 7
            risk_level = "Y√ºksek"
        else:
            risk_score = 9
            risk_level = "√áok Y√ºksek"
        
        # √ñnerilen pozisyon boyutu
        if risk_score <= 3:
            position_size = "Max %10"
        elif risk_score <= 5:
            position_size = "Max %5-7"
        elif risk_score <= 7:
            position_size = "Max %3-5"
        else:
            position_size = "Max %1-3"
        
        return {
            'volatility_pct': volatility_pct,
            'risk_score': risk_score,
            'risk_level': risk_level,
            'position_size': position_size,
            'atr': atr
        }
    except Exception as e:
        print(f"Risk hesaplama hatasƒ±: {e}")
        return {
            'volatility_pct': 0,
            'risk_score': 5,
            'risk_level': "Orta",
            'position_size': "Max %5",
            'atr': 0
        }

def calculate_support_resistance(df: pd.DataFrame, current_price: float) -> dict:
    """Geli≈ümi≈ü destek/diren√ß hesaplama"""
    try:
        # Fibonacci seviyeleri
        high = df['high'].tail(50).max()
        low = df['low'].tail(50).min()
        diff = high - low
        
        fib_levels = {
            '0%': high,
            '23.6%': high - diff * 0.236,
            '38.2%': high - diff * 0.382,
            '50%': high - diff * 0.5,
            '61.8%': high - diff * 0.618,
            '100%': low
        }
        
        # En yakƒ±n destek ve diren√ßleri bul
        support = None
        resistance = None
        
        for level, price in fib_levels.items():
            if price < current_price and (support is None or price > support):
                support = price
            elif price > current_price and (resistance is None or price < resistance):
                resistance = price
        
        # Pivot noktalarƒ±
        pivot = (df['high'].iloc[-1] + df['low'].iloc[-1] + df['close'].iloc[-1]) / 3
        r1 = 2 * pivot - df['low'].iloc[-1]
        s1 = 2 * pivot - df['high'].iloc[-1]
        
        return {
            'strong_support': support or s1,
            'strong_resistance': resistance or r1,
            'pivot': pivot,
            'fib_levels': fib_levels
        }
    except Exception as e:
        print(f"S/R hesaplama hatasƒ±: {e}")
        return {
            'strong_support': current_price * 0.95,
            'strong_resistance': current_price * 1.05,
            'pivot': current_price,
            'fib_levels': {}
        }

